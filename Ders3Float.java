
public class Ders3Float {

	public static void main(String[] args) {
/*                                                FLOAT VERİ TİPİ 
		
		                ONDALIKLI SAYI TANIMLAMAK İÇİN KULLANILIR NOKTADAN SONRA 7 BASAMAK ÇIKABİLİR.  -----> 4 BYTE
		                
		                
		                
*/
		
		
		
		//                             FLOAT VERİ TİPİNİN "Float.MAX_VALUE" MAX DEĞERİ.
		float sayı = Float.MAX_VALUE;
		System.out.println(sayı);
		
		
		
		//                             FLOAT VERİ TİPİNİN "Float.MIN_VALUE" MIN DEĞERİ.
		
		float sayı1 = Float.MIN_VALUE;
		System.out.println(sayı1);
		
		
		
		//                                ONDALIKLI FLOAT  SONUNA KÜÇÜK "f" HARFİ KONULUR FLOAT VERİ TİPİNDEN OLDUĞUNU GÖSTERİR
		
		
		
		float sayı2 = 3.5f;
		System.out.println(sayı2);
		
		
		
		//                                 FLOAT VERİ TİPİ NOKTADAN SONRA SADECE 7 BASAMAK EKRANA VERİRİ
		//                       FLOAT VERİ TİPİNDE ONDALIKLI KISIM "," DEĞİL "." İLE GÖSTERİLİR !!!
		
		float sayı4 = 5.5265846531165168f;
		System.out.println(sayı4);
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
